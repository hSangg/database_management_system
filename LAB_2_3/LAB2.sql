SELECT
    S_CUSTOMER.ID,
    S_ORD.ID
FROM
    S_CUSTOMER
    LEFT JOIN S_ORD
    ON S_CUSTOMER.ID=S_ORD.CUSTOMER_ID
ORDER BY
    S_CUSTOMER.ID;