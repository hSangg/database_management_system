    ID,
    LAST_NAME,
    COMMISSION_PCT
FROM
    S_EMP
WHERE
    COMMISSION_PCT=15;

--10)
SELECT
    LAST_NAME,
    START_DATE,
    TO_CHAR(ADD_MONTHS(TRUNC(START_DATE,
    'MM'),
    6)+8-MOD(TO_NUMBER(TO_CHAR(ADD_MONTHS(TRUNC(START_DATE,
    'MM'),
    6) + 8,
    'D')),
    7),
    'fmMonth DDth YYYY') AS NEXT_REVIEW_DATE
FROM
    S_EMP;


--12
SELECT
    LAST_NAME,
    FLOOR(MONTHS_BETWEEN(SYSDATE,
    START_DATE)) AS EXP
FROM
    S_EMP
ORDER BY
    EXP;

--13
SELECT
    COUNT(COUNT(*)) AS TOTAL_MANAGER
FROM
    S_EMP
GROUP BY
    MANAGER_ID
HAVING
    MANAGER_ID IS NOT NULL;

--14
SELECT
    MAX(TOTAL) AS HIGHEST,
    MIN(TOTAL) AS LOWEST
FROM
    S_ORD;

--15
SELECT
    PD.NAME,
    PD.ID,
    QUANTITY AS ORDERED
FROM
    S_PRODUCT PD,
    S_ORD     OD,
    S_ITEM    IT
WHERE
    IT.ORD_ID = OD.ID
    AND OD.ID = 101;

